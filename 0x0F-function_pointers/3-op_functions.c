#include "3-calc.h"

/**
 * op_add - adds two given integers
 * @a: first number
 * @b: second number
 * Return: the sum of the given integers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two given integers
 * @a: first number
 * @b: second number
 * Return: the diiference of the given integers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two given integers
 * @a: first number
 * @b: second number
 * Return: the product of the given integers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two given integers
 * @a: first number
 * @b: second number
 * Return: the result of division of the given integers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - adds two given integers
 * @a: first number
 * @b: second number
 * Return: the remainder of the division of the given integers
 */

int op_mod(int a, int b)
{
	return (a % b);
}
